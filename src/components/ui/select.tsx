// --- Compatibility Wrapper ---

interface SelectOption {
  label: string;
  value: string;
}

interface SelectProps extends React.ComponentPropsWithoutRef<typeof SelectPrimitive.Root> {
  options?: SelectOption[];
  placeholder?: string;
  label?: string;
  error?: string;
  fullWidth?: boolean;
  className?: string; // goes to Trigger
  onChange?: (value: string) => void; // alias for onValueChange
}

const Select = ({
  options,
  placeholder,
  label,
  error,
  fullWidth = false,
  className,
  value,
  onChange,
  onValueChange,
  disabled,
  ...props
}: SelectProps) => {
  // If no options provided, behave like Root (but this component returns a full structure, so not really possible to be purely Root unless we check children. simpler to assume if used as <Select>, it's likely the wrapper or compound usage)
  // Actually, Shadcn usage is <Select> <SelectTrigger>...</Select>
  // Backward compat usage is <Select options={...} />

  // Check if children exists, if so, act as Root
  if (props.children) {
    return <SelectRoot value={value} onValueChange={onChange || onValueChange} disabled={disabled} {...props} />;
  }

  return (
    <div className={cn("flex flex-col gap-1.5", fullWidth ? "w-full" : "w-auto")}>
      {label && (
        <label className="text-sm font-semibold text-foreground/70 ml-0.5">
          {label}
        </label>
      )}
      <SelectRoot value={value} onValueChange={onChange || onValueChange} disabled={disabled} {...props}>
        <SelectTrigger className={cn(className, error && "border-destructive")}>
          <SelectValue placeholder={placeholder} />
        </SelectTrigger>
        <SelectContent>
          {options?.map((opt) => (
            <SelectItem key={opt.value} value={opt.value}>
              {opt.label}
            </SelectItem>
          ))}
        </SelectContent>
      </SelectRoot>
      {error && <span className="text-[11px] font-medium text-destructive ml-0.5">{error}</span>}
    </div>
  );
};

export {
  Select,
  SelectRoot,
  SelectGroup,
  SelectValue,
  SelectTrigger,
  SelectContent,
  SelectLabel,
  SelectItem,
  SelectSeparator,
  SelectScrollUpButton,
  SelectScrollDownButton,
}
